package CustomPlayerDrop.Sandrix.Dev.Config;

public class Items {
	
	String mat;
	int count;
	
	public Items(String mat, int count){
		
		this.mat = mat;
		this.count = count;
		
	}
	
	public String getMat() {
		return mat;
	}
	
	public int getCount() {
		return count;
	}

}
